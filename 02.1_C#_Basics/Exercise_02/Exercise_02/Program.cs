using System;

namespace Exercise_02
{
	class Program
	{
		static void Main(string[] args)
		{
			ushort a = 52130;
			Console.WriteLine ("a = {0} is an ushort variable.\n", a);//52130

			sbyte b = -115;
			Console.WriteLine ("b = {0} is an sbyte variable.\n", b);//-115

			uint c = 4825932;
			Console.WriteLine ("c = {0} is an uint variable.\n", c);//4825932

			sbyte d = 97;
			Console.WriteLine ("d = {0} is an sbyte variable.\n", d);//97

			short e = -10000;
			Console.WriteLine ("e = {0} is an short variable.\n", e);//-10000

			short f = 20000;
			Console.WriteLine ("f = {0} is an short variable.\n", f);//20000

			byte g = 224;
			Console.WriteLine ("g = {0} is an byte variable.\n", g);//224

			int h = 970700000;
			Console.WriteLine ("h = {0} is an int variable.\n", h);//970700000

			byte i = 112;
			Console.WriteLine ("i = {0} is an byte variable.\n", i);//112

			sbyte l = -44;
			Console.WriteLine ("l = {0} is an sbyte variable.\n", l);//-44

			int m = -1000000;
			Console.WriteLine ("m = {0} is an int variable.\n", m);//-1000000

			short n = 1990;
			Console.WriteLine ("n = {0} is an short variable.\n", n);//1990

			long o = 123456789123456789;//ulong
			Console.WriteLine ("o = {0} is an long variable.\n", o);//123456789123456789
		}
	}
}
